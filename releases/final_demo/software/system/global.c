void check_memory(){
	uint32_t 	count;
	alt_u8* voidpte;

	count = 0;
	while(1){
		voidpte = malloc(1024);
		count++;
		if(voidpte == NULL){
			ssd_write_value(0xdddd0804);
			WAIT_FOR_INTERRUPT;
			ssd_write_value(count);
			WAIT_FOR_INTERRUPT;
			exit(-1);
		}
	}
}
